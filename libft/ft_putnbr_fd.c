#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		c;
	int		num;
	int		dig;

	n == -2147483648 ? write(fd, "-2147483648", 11) : 0;
	if (n < 0 && n != -2147483648)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	num = n;
	c = 1;
	while (num / 10 != 0 && n != -2147483648)
	{
		num /= 10;
		c *= 10;
	}
	while (c != 0 && n != -2147483648)
	{
		dig = n / c + 48;
		write(fd, &dig, 1);
		n %= c;
		c /= 10;
	}
}
